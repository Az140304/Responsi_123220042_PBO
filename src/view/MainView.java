    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
       try {
            dc.insert();
            dc.isitabel();
            showMessageDialog(null, "Data Berhasil Dimasukkan");
            
        } catch (NumberFormatException nfe) {
            showMessageDialog(null, "Data Gagal Dimasukkan");
        }     
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        try {
            dc.update();
            dc.isitabel();
            showMessageDialog(null, "Data Berhasil Diubah");
            
        } catch (NumberFormatException nfe) {
            showMessageDialog(null, "Data Gagal Diubah");
        }  
    }//GEN-LAST:event_btnUbahActionPerformed

    private void jTablePerpusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablePerpusMouseClicked
        // TODO add your handling code here:
        int baris = jTablePerpus.getSelectedRow();
        jTextJudul.setText(jTablePerpus.getValueAt(baris, 0).toString());
        jTextPenulis.setText(jTablePerpus.getValueAt(baris, 1).toString());
        jTextRating.setText(jTablePerpus.getValueAt(baris, 2).toString());
        jTextHarga.setText(jTablePerpus.getValueAt(baris, 3).toString());
        
    }//GEN-LAST:event_jTablePerpusMouseClicked

